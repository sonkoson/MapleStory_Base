var status = -1;

function start() {
    status = -1;
    action(1, 0, 0);
}

function action(mode, type, selection) {
    if (mode == -1 || mode == 0) {
        cm.dispose();
        return;
    }
    if (mode == 1) {
        status++;
    }

    if (status == 0) {
        cm.sendYesNo("ปี๊บ ปี๊บ~ ข้าสามารถพาท่านออกไปสู่พื้นที่ปลอดภัยได้\r\n"
            + "ปี๊บ ปี๊บ~ ต้องการออกไปข้างนอกตอนนี้เลยหรือไม่?");
    } else if (status == 1) {
        cm.warp(220080000);
        cm.dispose();
    }
}